import React from 'react'

import { Dropdown, ImagePreview, Modal, Toast, Tabs, TabPane, Icon, Button, Tooltip, Space } from '@douyinfe/semi-ui'

import ConfigPanel from './panels/config/ConfigPanel.jsx'
import ComponentListing from './panels/component/ComponentListing.jsx'
import AppFileList from './panels/files/AppFileList.jsx'
import OutLineTree from './panels/outline/OutLineTree.jsx'
import DialogCodeEdit from './panels/files/DialogCodeEdit.jsx'
import EditMenuBar from './panels/menu/EditMenuBar.jsx'
import context from './service/RidgeEditorContext.js'

import IconMultiFile from './icons/StreamlineMultipleFile2.svg'
import FluentAppsAddIn28Filled from './icons/FluentAppsAddIn28Filled.svg'
import IconClist from './icons/CilList.svg'
import IconSidePanelClose from './icons/CarbonSidePanelClose.svg'

import { ReactComposite } from 'ridgejs'

import './editor.less'
import PreviewMenuBar from './panels/menu/PreviewMenuBar.jsx'
const THEMES = [{
  label: '默认',
  value: '@douyinfe/semi-ui/dist/css/semi.min.css'
}, {
  label: '抖音',
  value: '@semi-bot/semi-theme-doucreator/semi.min.css'
}, {
  label: '飞书',
  value: '@semi-bot/semi-theme-universedesign/semi.min.css'
}, {
  label: 'Strapi',
  value: '@semi-bot/semi-theme-strapi/semi.min.css'
}, {
  label: '深蓝',
  value: '@semi-bot/semi-theme-ultim-dark-blue/semi.min.css'
}, {
  label: '剪映',
  value: '@semi-bot/semi-theme-jianying/semi.min.css'
}]
// 公用错误提示方法
globalThis.msgerror = msg => {
  Toast.error(msg)
}
globalThis.success = Toast.success
class Editor extends React.Component {
  constructor (props) {
    super(props)

    this.workspaceRef = React.createRef()
    this.viewPortContainerRef = React.createRef()
    this.codeEditorRef = React.createRef()

    this.state = {
      isLight: window.localStorage.getItem('ridge-is-light') !== 'false',
      pageOpened: false,
      collapseLeft: false,
      leftResizing: false,
      isPreview: false,
      leftReisizeWidth: 300,
      editPreview: false,

      imagePreviewSrc: null,
      imagePreviewVisible: false,

      // code preview/edit
      codeEditTitle: '',
      codeEditText: '',
      codeEditVisible: false,
      codeEditType: ''
    }

    context.setLight(this.state.isLight)
  }

  componentDidMount () {
    context.editorDidMount(this, this.workspaceRef.current, this.viewPortContainerRef.current)
  }

  handleLeftResize () {
    document.addEventListener('mousemove', ev => {
      if (this.state.leftResizing) {
        if (ev.clientX > 250) {
          this.setState({
            leftReisizeWidth: ev.clientX
          })
        }
      }
    })
    document.addEventListener('mouseup', ev => {
      this.setState({
        leftResizing: false
      })
    })
  }

  setPageOpened (opened) {
    this.setState({
      pageOpened: opened
    })
  }

  toggleLeftCollapse () {
    this.setState({
      collapseLeft: !this.state.collapseLeft
    })
  }

  setPreview (preview) {
    this.setState({
      isPreview: preview
    })
  }

  async confirm (mssage) {
    return new Promise((resolve, reject) => {
      Modal.confirm({
        zIndex: 10001,
        title: '确认',
        content: mssage,
        onOk: async () => {
          resolve()
        },
        onCancel: () => {
          reject(new Error())
        }
      })
    })
  }

  async message (msg) {
    Modal.message(msg)
  }

  setEditorLoaded () {
    this.setState({
      editorLoading: false
    }, () => {
      this.handleLeftResize()
    })
  }

  openInCodeEditor (file) {
    this.currentEditFile = file

    this.codeEditorRef.current?.openFile(file)
    // this.setState({
    //   codeEditTitle: file.name,
    //   codeEditText: file.textContent,
    //   codeEditVisible: true,
    //   codeEditType: file.mimeType
    // })
  }

  completeCodeEdit (code) {
    context.onCodeEditComplete(this.currentEditFile.id, code)
  }

  openImage (url) {
    this.setState({
      imagePreviewSrc: url,
      imagePreviewVisible: true
    })
  }

  setIsLight (isLight) {
    this.setState({
      isLight
    })
  }

  render () {
    const {
      state,
      workspaceRef,
      codeEditorRef,
      viewPortContainerRef
    } = this

    const {
      isLight,
      isPreview,
      collapseLeft,
      pageOpened,
      imagePreviewVisible,
      leftReisizeWidth,
      imagePreviewSrc,
      codeEditTitle
    } = state
    return (
      <>
        <div
          className='editor-root' style={{
            display: isPreview ? 'none' : ''
          }}
        >
          <Tabs
            className='root-nav'
            style={{
              width: collapseLeft ? '58px' : (leftReisizeWidth + 'px')
            }}
            onTabClick={(key, ev) => {
              this.setState({
                collapseLeft: false
              })
            }}
            tabPosition='left' type='button' tabBarExtraContent={
              <Space vertical>
                <Tooltip content='夜间/日间模式'>
                  <Button
                    type='tertiary'
                    theme='borderless'
                    icon={isLight ? <i className='bi bi-moon-stars-fill' /> : <i className='bi bi-sun' />}
                    onClick={() => {
                      this.setState({
                        isLight: !isLight
                      })
                      context.setLight(!isLight)
                    }}
                  />
                </Tooltip>

                <Dropdown
                  trigger='click'
                  position='right'
                  render={
                    <Dropdown.Menu>
                      {THEMES.map(theme =>
                        <Dropdown.Item
                          key={theme.value}
                          onClick={() => {
                            context.setTheme(theme.value)
                          }}
                        >{theme.label}
                        </Dropdown.Item>)}
                    </Dropdown.Menu>
            }
                >
                  <Button icon={<i className='bi bi-palette2' />} theme='borderless' size='small' type='tertiary' />
                </Dropdown>
                <Button
                  icon={<IconSidePanelClose />} onClick={() => {
                    this.setState({
                      collapseLeft: !collapseLeft
                    })
                  }}
                />
              </Space>
              }
          >
            <TabPane tab={<Tooltip content='应用文件管理' position='rightTop'> <Icon size='default' svg={<IconMultiFile />} /></Tooltip>} itemKey='components'>
              <AppFileList />
            </TabPane>
            <TabPane tab={<Tooltip content='组件面板' position='rightTop'> <Icon svg={<FluentAppsAddIn28Filled />} /></Tooltip>} itemKey='app'>
              <ComponentListing />
            </TabPane>
            <TabPane tab={<Tooltip content='页面大纲视图' position='rightTop'><Icon svg={<IconClist />} /> </Tooltip>} itemKey='outline'>
              <OutLineTree />
            </TabPane>
          </Tabs>
          {!collapseLeft &&
            <div
              className='left-resizer' onMouseDown={e => {
                e.preventDefault()
                this.setState({
                  leftResizing: true
                })
              }}
            />}
          <div className='editor-content'>
            <EditMenuBar />
            <div className='workspace-panel'>
              <div ref={workspaceRef} className='workspace'>
                <div className='view-port' ref={viewPortContainerRef} />
                {
                    !pageOpened && <div className='no-open-file'><ReactComposite app='ridge-editor-app' path='Welcome' /></div>
                }
              </div>
              {pageOpened && <ConfigPanel />}
            </div>
          </div>
          <div />
        </div>

        <div
          className='preview-root' style={{
            display: isPreview ? '' : 'none'
          }}
        >
          <PreviewMenuBar />
          <div className='preview-container'>
            <div className='preview-view-port' />
          </div>
        </div>

        <ImagePreview
          src={imagePreviewSrc} visible={imagePreviewVisible} onVisibleChange={() => {
            this.setState({
              imagePreviewVisible: false
            })
          }}
        />
        <DialogCodeEdit
          ref={codeEditorRef}
          title={codeEditTitle}
          onClose={() => {
            this.setState({
              codeEditVisible: false
            })
          }}
        />
      </>
    )
  }
}

export default Editor
